/**
 * @file wit.h
 * @brief 陀螺仪（WIT IMU）驱动头文件
 *
 * 硬件说明：
 *   - 传感器类型：WIT IMU（惯性测量单元）
 *   - 通信接口：UART串口
 *   - 数据传输：DMA接收
 *   - 数据内容：三轴加速度、三轴角速度、角度、温度等
 *
 * 数据结构说明：
 *   - pitch: 俯仰角（X轴旋转）
 *   - roll: 横滚角（Y轴旋转）
 *   - yaw: 偏航角（Z轴旋转）
 *   - ax/ay/az: 三轴加速度（原始值）
 *   - gx/gy/gz: 三轴角速度（原始值）
 *   - temperature: 温度
 *   - version: 数据版本
 *
 * DMA说明：
 *   - 使用DMA自动接收UART数据
 *   - 接收缓冲区：wit_dmaBuffer[33]
 *   - 接收长度：32字节
 */

#ifndef __WIT_H
#define __WIT_H

/*================================== 头文件引用 =====================================*/
#include "ti_msp_dl_config.h"  /* TI MSPM0 芯片配置（UART、DMA外设定义等）*/

/*================================== 陀螺仪数据结构 =====================================*/
/**
 * @brief 陀螺仪数据结构
 *
 * 包含IMU输出的所有数据：
 *   - 角度数据（pitch, roll, yaw）：单位度
 *   - 加速度数据（ax, ay, az）：原始ADC值
 *   - 角速度数据（gx, gy, gz）：原始ADC值
 *   - 温度数据：单位摄氏度
 */
typedef struct {
    float pitch;      /**< 俯仰角（X轴旋转角度）*/
    float roll;       /**< 横滚角（Y轴旋转角度）*/
    float yaw;        /**< 偏航角（Z轴旋转角度）*/
    float temperature; /**< 温度值（摄氏度）*/
    int16_t ax;       /**< X轴加速度（原始值）*/
    int16_t ay;       /**< Y轴加速度（原始值）*/
    int16_t az;       /**< Z轴加速度（原始值）*/
    int16_t gx;       /**< X轴角速度（原始值）*/
    int16_t gy;       /**< Y轴角速度（原始值）*/
    int16_t gz;       /**< Z轴角速度（原始值）*/
    int16_t version;   /**< 数据版本号 */
} WIT_Data_t;

/*================================== 全局变量 =====================================*/
/**
 * @brief 陀螺仪数据全局变量
 * @note 由DMA中断或数据解析函数更新
 */
extern WIT_Data_t wit_data;

/*================================== 函数声明 =====================================*/

/**
 * @brief 陀螺仪初始化
 *
 * 功能说明：
 *   - 配置DMA通道，从UART接收陀螺仪数据
 *   - 启用UART接收中断
 *
 * @note 初始化后陀螺仪数据会自动通过DMA接收
 */
void WIT_Init(void);

#endif /* __WIT_H */
