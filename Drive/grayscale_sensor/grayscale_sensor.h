/**
 * @file grayscale_sensor.h
 * @brief 八路灰度传感器驱动头文件
 *
 * 硬件说明：
 *   - 传感器类型：红外反射式灰度传感器
 *   - 传感器数量：8个（L1-L4左4个，R1-R4右4个）
 *   - 输出方式：数字量（检测到黑线输出1，白色输出0）
 *
 * 传感器布局（俯视图，小车前进方向向上）：
 *   L4 L3 L2 L1    R1 R2 R3 R4
 *   [8][7][6][5]  [4][3][2][1]
 *
 * 位置计算：
 *   - 使用加权平均法计算黑线中心位置
 *   - 位置值范围：1~8
 *   - 位置值4.5表示小车居中于黑线
 *
 * 使用说明：
 *   - 调用 Graysccale_Sensor_PinCheck() 采样并计算位置
 *   - distance 变量存储计算结果
 */

#ifndef __GRAYSCALE_ENSOR_H__
#define __GRAYSCALE_ENSOR_H__

/*================================== 头文件引用 =====================================*/
#include "ti_msp_dl_config.h"      /* TI MSPM0 芯片配置（GPIO引脚定义等）*/
#include "oled_software_i2c.h"   /* OLED显示（调试输出用）*/

/*================================== 全局变量 =====================================*/
/**
 * @brief 灰度传感器检测到的黑线位置
 *
 * 取值范围：1.0 ~ 8.0（加权平均计算结果）
 *   - 1.0：黑线在最左侧（L4传感器上方）
 *   - 4.5：黑线在中间（左右各两个传感器中间）
 *   - 8.0：黑线在最右侧（R4传感器上方）
 *
 * @note 由 Graysccale_Sensor_PinCheck() 函数更新
 */
extern float distance;

/*================================== 函数声明 =====================================*/

/**
 * @brief 灰度传感器采样与位置计算
 *
 * 功能说明：
 *   - 读取8个灰度传感器的数字输出
 *   - 使用加权平均法计算黑线中心位置
 *   - 保存上次有效值用于异常处理
 *
 * 加权平均计算：
 *   position = Σ(传感器编号 × 传感器值) / 检测到的传感器数量
 *   编号：L4=1, L3=2, L2=3, L1=4, R1=5, R2=6, R3=7, R4=8
 *
 * 异常处理：
 *   - 当所有传感器都未检测到黑线时（number=0），使用上次的有效值
 *   - 防止除零错误
 *
 * @note 此函数由灰度传感器定时器中断调用
 */
void Graysccale_Sensor_PinCheck(void);

#endif /* __GRAYSCALE_ENSOR_H__ */
