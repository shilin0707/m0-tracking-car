/**
 * @file Buzzer.h
 * @brief 蜂鸣器驱动头文件
 *
 * 硬件说明：
 *   - 蜂鸣器类型：无源蜂鸣器（需要PWM驱动）
 *   - 控制方式：GPIO引脚控制
 *   - 用途：按键提示、报警提示等
 *
 * 函数说明：
 *   - Buzzer_ON(): 打开蜂鸣器（持续响）
 *   - Buzzer_OFF(): 关闭蜂鸣器
 *   - Buzzer_Delay_ON(): 蜂鸣器响一段时间后自动关闭（提示音）
 */

#ifndef _BUZZER_H_
#define _BUZZER_H_

/*================================== 头文件引用 =====================================*/
#include "ti_msp_dl_config.h"  /* TI MSPM0 芯片配置（GPIO引脚定义等）*/
#include "clock.h"             /* 系统时钟和延时函数（mspm0_delay_ms）*/

/*================================== 宏定义 =====================================*/
#define DELAY_TIME     100   /**< 蜂鸣器提示音持续时间（单位：ms）*/

/*================================== 全局标志 =====================================*/
/**
 * @brief 蜂鸣器状态标志
 * @note 可用于外部检查蜂鸣器是否正在响
 */
extern uint8_t BUZZER_ON;

/*================================== 函数声明 =====================================*/

/**
 * @brief 打开蜂鸣器
 * @note 蜂鸣器将持续响，直到调用 Buzzer_OFF()
 */
void Buzzer_ON(void);

/**
 * @brief 关闭蜂鸣器
 * @note 蜂鸣器停止响
 */
void Buzzer_OFF(void);

/**
 * @brief 蜂鸣器提示音
 * @note 蜂鸣器响一段时间（约100ms）后自动关闭
 *       用于按键提示或简短报警
 */
void Buzzer_Delay_ON(void);

#endif /* _BUZZER_H_ */
