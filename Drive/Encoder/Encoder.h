/**
 * @file Encoder.h
 * @brief 编码器驱动头文件
 *
 * 硬件说明：
 *   - 编码器类型：AB相增量式编码器
 *   - 分辨率：728脉冲/转（单端计数，经过4倍频）
 *   - 接口：两组编码器（左右各一组），每组使用两个GPIO引脚（A和B相）
 *
 * 工作原理：
 *   - AB相编码器输出两路相位差90°的方波信号
 *   - 通过检测A相和B相的边沿变化判断转向
 *   - A相上升沿时，如果B相为高电平则正转，否则反转
 *
 * 计数器说明：
 *   - Encoder_L：左编码器累计脉冲数（带符号）
 *   - Encoder_R：右编码器累计脉冲数（带符号）
 *   - 向前走时，一个轮子正转，另一个反转（轮子对着安装）
 */

#ifndef __ENCODER_H__
#define __ENCODER_H__

/*================================== 头文件引用 =====================================*/
#include "ti_msp_dl_config.h"  /* TI MSPM0 芯片配置（GPIO引脚定义等）*/

/*================================== 宏定义 =====================================*/
#define ENCODER_MAX_CNT  728   /**< 编码器单圈脉冲数（单端计数）*/

/*================================== 全局变量 =====================================*/
/**
 * @brief 编码器累计计数值
 * @note 由 Encoder_Check() 在编码器外部中断中更新
 *       正值表示正转（前进方向），负值表示反转（后退方向）
 */
extern int32_t Encoder_L;   /**< 左编码器累计脉冲数 */
extern int32_t Encoder_R;   /**< 右编码器累计脉冲数 */

/*================================== 函数声明 =====================================*/

/**
 * @brief 编码器计数处理
 *
 * 功能说明：
 *   - 检测四个编码器引脚的中断状态
 *   - 根据AB相信号判断转向并更新计数值
 *   - 清除中断标志
 *
 * 判断逻辑：
 *   - 左轮：轮子对着安装，前进时左轮反转计数减小
 *     - A相上升沿 + B相低电平 -> 计数减
 *     - A相上升沿 + B相高电平 -> 计数加
 *   - 右轮：前进时右轮正转计数增大
 *     - A相上升沿 + B相低电平 -> 计数加
 *     - A相上升沿 + B相高电平 -> 计数减
 *
 * @note 此函数由外部中断（GROUP1_IRQHandler）调用
 */
void Encoder_Check(void);

#endif /* __ENCODER_H__ */
